    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            int nomor = Integer.parseInt(fieldNoBarang.getText());
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/k05?user=root&password=root");
            if(ConstUtil.auth.getRole().equals("Admin")){
                ps = con.prepareStatement("SELECT no_barang AS NomorBarang, nama_barang AS NamaBarang, waktu AS Waktu, ciri AS CiriCiri FROM barang WHERE no_barang = ?");
                ps.setString(1, String.valueOf(nomor));
                
            }
            else{
                ps = con.prepareStatement("SELECT no_barang AS NomorBarang, nama_barang AS NamaBarang, waktu AS Waktu, ciri AS CiriCiri FROM barang WHERE no_barang = ? AND nim = ?");
                ps.setString(1, String.valueOf(nomor));
                ps.setString(2, ConstUtil.auth.getNim());
            }
            ResultSet rs = ps.executeQuery();
            jTable2.setModel(DbUtils.resultSetToTableModel(rs));
            rs.beforeFirst();
            if(!rs.next()){
                if(!rs.next()){
                    JOptionPane.showMessageDialog(rootPane, "Barang laporan anda tidak ditemukan");
                }
            }
            else{
                try{
                    con = DriverManager.getConnection("jdbc:mysql://localhost:3306/k05?user=root&password=root");
                    ps = con.prepareStatement("INSERT INTO barang_balik(no_barang, pembuat_laporan, nim_pembuat_laporan, no_hp) VALUES(?,?,?,?)");
                    ps.setString(1, rs.getString(1));
                    ps.setString(2, ConstUtil.auth.getNama());
                    ps.setString(3, ConstUtil.auth.getNim());
                    ps.setString(4, ConstUtil.auth.getNoHp());
                    ps.executeUpdate();
                    con.close();
                    JOptionPane.showMessageDialog(rootPane, "Laporan Barang kembali berhasil dikirim ke admin");
                }
                
                catch(Exception ex){
                    JOptionPane.showMessageDialog(rootPane, "Laporan untuk barang ini, sudah dikirim");
                }
            }
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(rootPane, "Masukkan Angka");
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

